import turtle as t

t.shape('turtle')

t.fillcolor('yellow')
t.begin_fill()
t.circle(100)
t.end_fill()
t.up()
t.goto(-50, 125)
t.down()
t.fillcolor('blue')
t.begin_fill()
t.circle(10)
t.end_fill()
t.up()
t.goto(50, 125)
t.down()
t.fillcolor('blue')
t.begin_fill()
t.circle(10)
t.end_fill()
t.up()
t.goto(0, 125)
t.down()
t.width(10)
t.goto(0, 75)
t.up()
t.goto(75, 100)
t.down()
t.pencolor('red')
t.right(90)
t.circle(-75, 180)
